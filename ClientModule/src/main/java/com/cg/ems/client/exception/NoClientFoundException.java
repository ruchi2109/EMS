package com.cg.ems.client.exception;

public class NoClientFoundException extends Exception{

	public NoClientFoundException() {
		super();
	}

	public NoClientFoundException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

	public NoClientFoundException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public NoClientFoundException(String arg0) {
		super(arg0);
	}

	public NoClientFoundException(Throwable arg0) {
		super(arg0);
	}
	

}
